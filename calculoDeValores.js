function somarDoisValores(x, y) {
  return x + y;
}

function subtrairDoisValores(x, y) {
  return x - y;
}

function multiplicarDoisValores(x, y) {
  return x * y;
}

function dividirDoisValores(x, y) {
  return x / y;
}

module.exports = {
  somarDoisValores,
  subtrairDoisValores,
  multiplicarDoisValores,
  dividirDoisValores,
}